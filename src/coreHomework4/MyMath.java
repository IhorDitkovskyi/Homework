package coreHomework4;

public class MyMath {

	 public static final double PI = 3.14;
	 public static final int CONST2 = 5;

	 
	 public static double Rad (int R){
		 return PI*(R*R);
	 }
	 
	 public static double sub (int R){
		 return PI*PI ;
	 }
	 
	 public static double mul (){
		 return PI*CONST2;
	 }
	 
	 public static  double div (){
		 return PI/CONST2;
	 }
	 
}